// Map browser language code to supported locale
function detectSystemLocale(): Locale {
  const browserLang = navigator.language;
  const saved = localStorage.getItem('ariadne_locale') as Locale | null;
  if (saved && saved in BUNDLES) return saved;

  // Map browser language to our supported locales
  const langMap: Record<string, Locale> = {
    'zh-Hans': 'zh_CN', 'zh-CN': 'zh_CN', 'zh': 'zh_CN',
    'zh-Hant': 'zh_TW', 'zh-TW': 'zh_TW', 'zh-HK': 'zh_TW',
    'ja': 'ja',
    'fr': 'fr',
    'es': 'es',
    'ru': 'ru',
    'ar': 'ar',
    'en': 'en',
  };

  return langMap[browserLang] ?? langMap[browserLang.split('-')[0]] ?? 'en';
}

// Initialize i18n from localStorage or system language
export function initI18n(): Locale {
  currentLocale = detectSystemLocale();
  return currentLocale;
}

export function setLocale(locale: Locale): void {
  currentLocale = locale;
  localStorage.setItem('ariadne_locale', locale);
  // Dispatch event for all components to re-render
  window.dispatchEvent(new CustomEvent('localechange', { detail: { locale } }));
}

export function getLocale(): Locale {
  return currentLocale;
}

export function t(key: string, fallback?: string): string {
  return BUNDLES[currentLocale]?.[key] ?? BUNDLES.en[key] ?? fallback ?? key;
}

export function getLocaleInfo(code: Locale): LocaleInfo {
  return LOCALES.find((l) => l.code === code) ?? LOCALES[0];
}
